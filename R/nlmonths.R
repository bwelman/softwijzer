#' @title Dutch names for the months of the year.
#'
#' @description
#'
#' `month.abb.nl` three-letter abbreviations
#' `month.name.nl` full names
#'
#' @return character
#'
#' @examples
#' month.abb.nl
#' month.abb.nl[1:3]
#' month.name.nl
#' month.name.nl[4:6]
#'
#' @name Dutch_Month_Names
NULL

#' @describeIn Dutch_Month_Names three-letter abbreviations
#' @export
month.abb.nl <- c("jan", "feb", "mrt", "apr", "mei", "jun", "jul", "aug", "sep", "okt", "nov", "dec")

#' @describeIn Dutch_Month_Names full names
#' @export
month.name.nl <- c("januari", "februari", "maart", "april", "mei", "juni", "juli", "augustus", "september", "oktober", "november", "december")
